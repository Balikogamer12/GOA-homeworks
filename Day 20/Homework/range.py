# for ციკლი გამოიყენება კოდის განმეორებითად შესასრულებლად
# მაგალითად, თუ გვინდა რაღაც ქმედება X-ჯერ შესრულდეს

# საიტერაციო ცვლადი არის ცვლადი, რომელიც თითოეულ ბიჯზე იღებს ახალ მნიშვნელობას
# მაგალითად, i ცვლის მნიშვნელობას ყოველ ჯერზე: 0, 1, 2, ...

# range ფუნქცია ქმნის რიცხვების მიმდევრობას
