# == 
# != 
# > 
# < 
# >= 
# <= 

# == (შედარება)
print(5 == 5)  
print("rugby" == "ball")  
print(10 == 10)  

# != (არატოლი)
print(5 != 3)  
print("scrum" != "helmet")  
print(3 != 7)  

# > (უფრო დიდი)
print(14 > 5)  
print(6 > 10)  
print(4 > 2)  

# < (უფრო პატარა)
print(3 < 5)  
print(10 < 7)  
print(2 < 9)  
# >= (უფრო დიდი ან ტოლი)
print(10 >= 5)  
print(3 >= 5)  
print(7 >= 7)  

# <= (უფრო პატარა ან ტოლი)
print(5 <= 10)  # True
print(7 <= 4)  # False
print(8 <= 8)  # True




